int			ft_is_prime(int nb)
{
	int		div_num;

	div_num = 1;
	if (nb == 0 || nb == 1 || nb < 0)
	{
		return (0);
	}
	while (++div_num <= nb / 2)
	{
		if (nb % div_num == 0)
		{
			return (0);
		}
	}
	return (1);
}
