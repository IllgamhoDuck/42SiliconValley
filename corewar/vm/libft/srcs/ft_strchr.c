#include "libft.h"

char	*ft_strchr(const char *s1, int c)
{
	while (*s1 != '\0')
	{
		if (*s1 == c)
			return ((char *)s1);
		s1++;
	}
	if (!c)
		return ((char *)s1);
	return (0);
}
