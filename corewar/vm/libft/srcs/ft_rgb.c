#include "libft.h"

uint32_t			ft_rgb(unsigned char r, unsigned char g, unsigned char b)
{
	uint32_t c;

	c = r;
	c <<= 8;
	c |= g;
	c <<= 8;
	c |= b;
	return (c);
}
