int			max(int *tab, unsigned int len)
{
	unsigned int	i;
	int				max;

	i = 0;
	max = -2147483648;
	if (!(tab))
		return (0);
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
